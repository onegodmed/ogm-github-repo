<strong>KARMA AND YOU</strong><br>

‘We reap what we sow’ the idiom that sums up the concept of Karma! The idea of Karma ingrained in human culture with a strong influence over the people living in different corners of the world. In general, Karma is the fruits of our deeds performed in our past life and thus, it is believed that our present life on the earth is leading by our past life. But there is a lot more to it, read on to know in detail, 
</p>

<p class="ast_blog_info_details">
<strong>The Karma Mechanism</strong><br>
Every action we do leaves an imprint and eventually, it gives rise to its own effect. Karma is actually a Sanskrit word for ‘action’ or ‘deeds’. Life is an infinite chain of results and deeds in the past. It is considered to be a spiritually originated law that presides over us.
</p>

<p class="ast_blog_info_details">
If the law of karma is to be believed, it is clear that an individual is sole doer and enjoyer of his Karmas and its fruits, and Vedanta says that the Supreme Being (The Divine) plays a major role as the dispenser of fruits of Karma. 
</p>

<p class="ast_blog_info_details">
<strong>‘What goes around comes around’- Karma is the longest traveler!</strong><br>
The law of Karma explains why each individual has a unique physical appearance, mental disposition, and experience. These are the various effects of countless action that each of them has performed individually in the past. One cannot find the two people, who have created the same series of actions in history and thus, two people never have an identical state of mind, identical experiences or identical physical appearance. 
</p>

<p class="ast_blog_info_details">
So, Karma is the guiding factor and the reason why some people are enjoying their good health and while others are constantly ill, some people have a happy and cheerful state of mind while others have a sour deposition and rarely delighted by anything, some people easily understand the meaning of spiritual learning while other find them tough and obscure. 
</p>

<p class="ast_blog_info_details">
This is how nature punishes and gives bliss! According to Vedas, if one sows goodness, will reap the same and if one sows evil, will reap the evil and the delivery of consequence is controlled by the divine concurrently. 
</p>

<p class="ast_blog_info_details">
<strong>Special practice of believing in Karma</strong><br>
Needless to say, Karma is a fundamental mechanism of our existence and eventually, it applies to everyone, regardless of belief! 
</p>

<p class="ast_blog_info_details">
Believing in Karma has no real effect but it is all about believing in healthy living without changing your bad habits. To find eternal peace and happiness, it is vital to keep our thought process inclined to those things which can bring their desires closer and Karma is being one of them. Also not to forget that the Karmic cycle plays a great part in shaping up the human life!
</p>

<p class="ast_blog_info_details">
<strong>Wrapping up…</strong><br>
In a nutshell, if you take an individual life as an isolated event which begins with birth and ceases with its death, you cannot find any appropriate explanation or solution for the affair of life. Understanding the Karma allows human to live in harmony with the people and condition that we bump into daily life. Also, recognizing that our actions determine our future happenings encourages us to live a positive life. To let you know more about your inner self, we, at onegodmed.com, strive to make your life better by making you understand about the fruits of Karma. 
